public class Aula2 {
    public static void main(String[] args) {
        int horas = 3; 
        int primera_hora = 10;
        int segunda_hora = 4;
        int terceira_hora = 2;
        int total_horas = primera_hora + segunda_hora + terceira_hora;
        double media = total_horas / horas;
        System.out.println("Total de horas: " + total_horas);
        System.out.println("Média de horas: " + media);
    }
}
